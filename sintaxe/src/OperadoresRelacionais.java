public class OperadoresRelacionais {
    public static void main(String[] args) {
        // Igual
        System.out.println(1 == 2);

        // Diferente
        System.out.println(1 != 2);

        // Maior
        System.out.println(1 > 2);

        // Menor
        System.out.println(1 < 2);

        // Maior ou igual
        System.out.println(1 >= 2);

        //Menor ou igual
        System.out.println(1 <= 2);
    }
}
